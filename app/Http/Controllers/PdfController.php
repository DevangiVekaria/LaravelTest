        $pdf = \App::make('dompdf.wrapper');
        $customPaper = array(0, 0, 850, 1100);
        $pdf->setPaper($customPaper);
        $pdf->setOptions(['isHtml5ParserEnabled' => true]);
        $pdf->loadView('pdf.pdfview');
        return $pdf->download('generatehtml.pdf');

